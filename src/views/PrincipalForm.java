    private void guardarMaterialBibliografico(String tipo){
        String titulo = txtTitulo.getText(),
               autor  = txtAutor.getText(),
            editorial = txtEditorial.getText(),
            categoria = cboCategoria.getSelectedItem().toString();
        switch(tipo){
            case "Libro":
                String descripcion = txtDescripcion.getText();
                String isbn = txtISBN.getText();
                Libro libro = new Libro(titulo, autor, editorial, categoria,descripcion, isbn);
                materialService.guardar(libro);
            break;
            case "Revista":
                int numArticulos = Integer.parseInt(txtNumArticulos.getText());
                Revista revista = new Revista(titulo, autor, editorial, categoria, numArticulos);
                materialService.guardar(revista);
            break;
            case "Tesis":
                String universidad = txtUniversidad.getText();
                String tutor = txtTutor.getText();
                Tesis tesis = new Tesis(titulo, autor, editorial, categoria, universidad, tutor);
                materialService.guardar(tesis);
                
            break;
            default:
                errorMessage.setText("Tipo de Material no valido");
                ErrorMessage.setVisible(true);
                break;
        }
        successMessage.setText(tipo+" Se agrego Correctamente");
        SuccessMessage.setVisible(true);
    }
    

