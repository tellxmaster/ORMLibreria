    private void cboMaterialcboMaterialItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboMaterialcboMaterialItemStateChanged
        switch(cboMaterial.getSelectedItem().toString()){
            case "Libro":
            revistaFields.setVisible(false);
            tesisFIelds.setVisible(false);
            libroFields.setVisible(true);
            cboCategoria.setModel(catLibro);
            break;
            case "Revista":
            
            revistaFields.setVisible(true);
            tesisFIelds.setVisible(false);
            libroFields.setVisible(false);
            cboCategoria.setModel(catRevista);
            break;
            case "Tesis":
            
            revistaFields.setVisible(false);
            tesisFIelds.setVisible(true);
            libroFields.setVisible(false);
            cboCategoria.setModel(catTesis);
            
            break;
            default:
            
            revistaFields.setVisible(false);
            tesisFIelds.setVisible(false);
            libroFields.setVisible(false);
            cboCategoria.removeAllItems();
            
            break;
        }
        //this.repaint();
    }//GEN-LAST:event_cboMaterialcboMaterialItemStateChanged

